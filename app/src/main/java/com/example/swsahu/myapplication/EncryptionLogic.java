    public static String ShuffleText(String text,String key)
    {
        String ShuffledText = "";
        int text_length = text.length();
        int shuffledLength = 0;
        int[] sh_code = SH_CODE[GetShuffleCode(key)];
        int sh_index = 0;
        int []hash = new int[text_length];

        while(shuffledLength<text_length-1)
        {
            for(int i = sh_code[sh_index];i<text_length;i+=sh_code[sh_index])
            {
                if(hash[i] == 0)
                {
                    ShuffledText += text.charAt(i);
                    shuffledLength++;
                    hash[i] = 1;
                }
            }
            sh_index++;
        }
        ShuffledText+=text.charAt(0);
        return ShuffledText;
    }

